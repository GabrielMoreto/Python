# Em Python temos os tipos de variáveis como por exemplo, string, inteiro, float e etc.
# Segue alguns exemplos abaixo:

# String:

# Em Python para que uma variável seja considerada uma string basta que você a passe entre "".

pais = "Brasil"

# Para validar o tipo da variável basta executar a função type().

print(type(pais))

# Int:

quantidade = 10
print(type(quantidade))

# Float:

preco = 2.99
print(type(preco))